float32_t calc_RMSE(uint32_t measured, uint32_t count) {
    float32_t sum = 0.0;
    float32_t error = 0;

    error = (float)(measured - TRUE_DIST);
    sum += error*error;

    RMSE = sqrtf(sum/(float)count);

    return RMSE;
}

float32_t calc_MAE(uint32_t measured, uint32_t count) {
    float32_t sum = 0.0;
    sum += fabsf((float)measured - (float)TRUE_DIST);
    MAE = sum / (float)count;

    return MAE;
}

float32_t calc_Rel_Err(uint32_t measured, uint32_t count) {
    float32_t sum = 0.0;
    sum += fabsf((float)measured - TRUE_DIST)/(float)TRUE_DIST;

    RelERR = (sum / (float)count) * 100.0f;

    return RelERR;
}

